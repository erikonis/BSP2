public class Person {
    private int id;
    private String name;

    // Constructor to initialize id and name
    public Person(int id, String name) {
        this.id = id;
        this.name = name;
    }

    // Getter methods for id and name
    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }
}
