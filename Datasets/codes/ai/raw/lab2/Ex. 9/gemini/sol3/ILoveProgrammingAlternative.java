public class ILoveProgrammingAlternative {
    public static void main(String[] args) {
        // Another way to write a for loop that iterates 20 times.
        // This version separates the loop counter declaration outside the loop.

        int i; // Declare the loop counter outside

        for (i = 0; i < 20; i++) {
            System.out.println("I love programming!");
        }
    }
}