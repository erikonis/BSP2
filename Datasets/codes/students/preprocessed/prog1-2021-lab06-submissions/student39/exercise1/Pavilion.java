/**
 * Pavilion
 */
public class Pavilion {
    private double price;
    private String name;
    Pavilion(double price, String name){
        this.price = price;
        this.name = name;}
    //sets a name and price for the pavillion

    public double getPrice(){
        return price;}

    public String getName(){
        return name;}
        //two getters for the variables
}