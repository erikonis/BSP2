public class Pavilion {

private String name;
private double price;



    public Pavilion(String name, int price) {
        this.name = name;
        this.price = price;
    }


    public String getName() {
        return this.name;
    }


    public void setName(String name) {
        this.name = name;
    }
    public void setPrice(double price) {
        this.price = price;
    }


    public double getPrice() {
        return this.price;
    }






}