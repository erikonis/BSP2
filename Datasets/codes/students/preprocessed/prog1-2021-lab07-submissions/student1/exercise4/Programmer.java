public class Programmer {
    public String name;
    public int age;
    public boolean teacher;
    public boolean student;

    public Programmer(String name, int age, boolean teacher, boolean student) {
        this.name = name;
        this.age = age;
        this.teacher = teacher;
        this.student = student;
    }
    public int getAge() {
        return age;
    }
    public String getName() {
        return name;
    }
   
    
 
    
    
}
