package Exercise2;

public class Order {
  
        
            private String name;
            private double price;
            private double amount;
            
            
            public Order(String name, double price, double amount) {
                this.name = name;
                this.price = price;
                this.amount = amount;
            }
        
        
            public String getName() {
                return name;
            }
        
        
            public void setName(String name) {
                this.name = name;
            }
        
        
            public double getPrice() {
                return price;
            }
        
        
            public void setPrice(double price) {
                this.price = price;
            }
        
        
            public double getAmount() {
                return amount;
            }
        
        
            public void setAmount(double amount) {
                this.amount = amount;
            }
        
        
            
            
        }
        
        

