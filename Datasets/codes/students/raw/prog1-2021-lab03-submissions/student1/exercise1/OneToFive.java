package lu.uni.programming1.lab3.exercise1;

public class OneToFive {
    public static void main(String[] args) {
        for(int i = 1; i < 6; i++){
            if (i < 5){
            System.out.print(i+ ", ");
            }else{
                System.out.print(i);
            }
        }
    }
}
