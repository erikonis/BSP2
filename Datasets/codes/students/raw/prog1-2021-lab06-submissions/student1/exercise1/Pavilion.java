package lu.uni.programming1.lab6.exercise1;

public class Pavilion {
    //atributes
    private String name;
    private double price;

    //constructor
    public Pavilion (String name, double price){
        this.name = name;
        this.price = price;

    //getters
    }
    public String getName() {
        return name;
    }
    public double getPrice() {
        return price;
    }

}
