package lu.uni.programming1.lab6.exercise1;

public class Pavilon {
    private String name;
	private double price;
	
	public Pavilon(String name_, double price_) {
		this.name = name_;
		this.price = price_;
	}

	public String getname() {
		return name;
	}

    public double getprice() {
		return price;
	}

}
